eventsInfo.sort((a, b) => {
	// Extract day as a number
	const dayA = parseInt(a.date.split(" ")[0]);
	const dayB = parseInt(b.date.split(" ")[0]);

	// Compare days
	if (dayA < dayB) {
		return -1;
	} else if (dayA > dayB) {
		return 1;
	} else {
		// If days are equal, compare times
		const timeA = new Date(`1970/01/01 ${a.time}`);
		const timeB = new Date(`1970/01/01 ${b.time}`);
		return timeA - timeB;
	}
});

